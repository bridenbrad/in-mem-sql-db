package com.tirion.db.store.builder.stats.analyzer.range;

import java.util.ArrayList;
import java.util.Arrays;
import java.util.Collections;
import java.util.List;

import com.tirion.common.sequence.array.Array;
import com.tirion.common.type.Type;
import com.tirion.db.store.builder.stats.model.range.Comparators;
import com.tirion.db.store.builder.stats.model.range.IntegerRange;
import com.tirion.db.store.builder.stats.model.range.Range;
import com.tirion.db.store.builder.stats.model.range.Ranges;

/**
 * This class was autogenerated. Do not edit manually.
 */
public final class IntegerRangeAnalyzer extends AbstractRangeAnalyzer {

	public IntegerRangeAnalyzer(RangesConf conf) {
		super(conf);
	}

	@Override
	public Ranges analyze(Array array) {
		List<IntegerRange> ranges = new ArrayList<IntegerRange>();
		int[] input = cloneAndSortArray((int[])array.getUnderlying());
		int index = 1;
		while(true) {
			if(index == input.length) {
				break;
			}
			int start = input[index - 1];
			int end = input[index];
			if(shouldIncludeSkipRange(start, end)) {
				ranges.add(new IntegerRange(start, end));
			}
			++index;
		}
		if(ranges.isEmpty()) {
			return null;
		}
		Ranges result = new Ranges(Type.INT);
		for(Range range : tuneSkipRanges(ranges)) {
			result.append(range);
		}
		return result;
	}
	
	private List<IntegerRange> tuneSkipRanges(List<IntegerRange> ranges) {
		if(ranges.size() <= getMaxSkipRangesPerPage()) {
			return ranges;
		}
		Collections.sort(ranges, new Comparators.IntegerRangeComparator());
		int count = Math.min(getMaxSkipRangesPerPage(), ranges.size());
		List<IntegerRange> result = new ArrayList<IntegerRange>();
		for (int i = ranges.size() - 1; i >= ranges.size() - count; i--) {
			result.add(ranges.get(i));
		}
		return result;
	}
	
	private boolean shouldIncludeSkipRange(int start, int end) {
		return (end - start - 1) >= 
					getSkipRangeDiscreteThreshold();
			}
	
	private int[] cloneAndSortArray(int[] input) {
		int[] clone = new int[input.length];
		System.arraycopy(input, 0, clone, 0, input.length);
		Arrays.sort(clone);
		return clone;
	}
}
